// 按名字模糊搜索精选 icon
export function searchIcons(query: string): string[] {
  const q = query.trim().toLowerCase()
  if (!q) return []
  return Array.from(ALL_ICON_NAMES).filter((name) =>
    name.toLowerCase().includes(q),
  )
}
